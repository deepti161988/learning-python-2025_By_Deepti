# Basic data types in Python: Numbers, Strings, Booleans 
# Variable names must start with a letter or _, and can have numbers. They are case sensitive. 
myint = 10
myfloat = 13.2576
mystr = "This is a string"
mybool = True

# We can display the content of a variable using the print() function
print(myint)
print(myfloat)


# Operators are used to perform operations on variables
print(myint + myfloat)
print(myint - myfloat)
print(myint / myfloat)
print(myint % myfloat)

# Logical and comparison operators 
x = 10
y = 5
print(x > y)  # True (x is greater than y)
print(x < y)  # False (x is not less than y)
print(x == y) # False (x is not equal to y)
print(x != y) # True (x is not equal to y)
print(x >= y) # True (x is greater than or equal to y)
print(x <= y) # False (x is not less than or equal to y)



